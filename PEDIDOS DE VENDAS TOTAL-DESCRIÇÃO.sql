--TOTAL PEDIDO DE VENDA

SELECT SUM(C6_VALOR) AS TOTAL FROM SC5010
INNER JOIN SC6010 ON C5_NUM = C6_NUM
where C5_NUM = '062839'

--DESCRIÇÃO PEDIDOS DE VENDAS

SELECT C5_NUM, C6_PRODUTO, C6_DESCRI, C6_QTDVEN, C6_PRCVEN, C6_VALOR FROM SC5010
INNER JOIN SC6010 ON C5_NUM = C6_NUM
where C5_NUM = '062839'
ORDER BY C6_DESCRI


