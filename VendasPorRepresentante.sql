SELECT  A3_NOME AS REPRESENTANTE, SUM (F2_VALBRUT) AS valor FROM SF2010
	INNER JOIN SA1010 ON A1_COD = F2_CLIENTE
	INNER JOIN SA3010 ON A3_COD = A1_VEND
	WHERE F2_EMISSAO BETWEEN '20160401' AND '20170331' 
	AND F2_TIPO = 'N'
	AND F2_DUPL <> '' 
	AND F2_CLIENTE <> '004273'
	AND F2_CLIENTE <> '007288'
	AND F2_CLIENTE <> '007289'
	AND SF2010.D_E_L_E_T_ = '' 
	AND A1_VEND IN ('000011','000125','000128','000097','000139','000122','000110','000053','000136','000123','000094','000138','000074','000103','000081','000137','000067','000141','000092','000116','000091','000108','000114','000111','000078','000113','000121','000013','000120','000106','000014','000118','000001','000127','000107','000140','000115','000124','000060','000126','000082','000068','000098','000133') 
	GROUP BY SA3010.A3_NOME
	ORDER BY SA3010.A3_NOME