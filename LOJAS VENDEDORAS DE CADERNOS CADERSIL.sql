-- Lojas represetantes por EST/MUN

SELECT DISTINCT A1_NOME NOME, A1_END ENDEREÇO, A1_TEL TELEFONE, A1_MUN MUNICIPIO, A1_EST ESTADO, F2_EMISSAO FROM SF2010
inner join SA1010 ON A1_COD = F2_CLIENTE
WHERE F2_EST = 'MG' 
AND A1_MUN = 'Juiz de Fora'
AND SF2010.D_E_L_E_T_ = ''
AND F2_EMISSAO BETWEEN '20150401' AND '20170112'
ORDER BY A1_MUN

-- Lojas que vedem produtos especificos

SELECT DISTINCT A1_NOME, A1_END, A1_TEL, A1_MUN, A1_EST, C6_PRODUTO, C6_DESCRI, C6_ENTREG, C6_CLI FROM SC6010
inner join SA1010 ON A1_COD = C6_CLI
WHERE C6_PRODUTO IN ('0705001','0705002','0705003','0705004','0705006')
AND C6_ENTREG BETWEEN '20150401' AND '20170112'
AND SC6010.D_E_L_E_T_='' 
AND C6_NOTA <> ''
AND A1_EST = 'BA' 
AND A1_MUN IN ('SALVADOR','CAMACARI')
ORDER BY C6_ENTREG

