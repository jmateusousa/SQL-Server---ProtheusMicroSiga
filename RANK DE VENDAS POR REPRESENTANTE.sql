SELECT TOP 12 A3_NOME REPRESENTANTE , A1_NOME CLIENTE, SUM (F2_VALBRUT) VALOR_TOTAL FROM SF2010,SA1010,SA3010
WHERE F2_CLIENTE = A1_COD AND A1_VEND = A3_COD
AND SF2010.D_E_L_E_T_ = '' 
AND F2_DUPL <> '' 
AND F2_EMISSAO BETWEEN '20160401' AND '20170331'
GROUP BY A3_NOME,A1_NOME 
ORDER BY VALOR_TOTAL DESC