SELECT C5_CLIENTE, A1_NOME, C5_NUM, SUM(C6_VALOR) VALOR, C5_D2TRANS DATA_FATURAMENTO, C5_MENPED OBS, C5_EMISSAO from SC5010
JOIN SC6010 ON C6_NUM = C5_NUM
JOIN SA1010 ON A1_COD = C5_CLIENTE
WHERE C5_NOTA = ''
AND C6_NOTA = ''
AND C5_VEND1 = '000082'
AND C5_EMISSAO > '20160101'
AND SC5010.D_E_L_E_T_ <> '*'
GROUP BY C5_CLIENTE, A1_NOME, C5_NUM, C5_D2TRANS, C5_MENPED, C5_EMISSAO 
ORDER BY A1_NOME 




